package com.tv.trainings;

public class Fruits {
	String color;
	String size;
	int price;

	Fruits(String color, String size, int price) {
		this.color = color;
		this.size = size;
		this.price = price;
	}

	public String getColor() {
		return color;
	}

	public String getSize() {
		return size;
	}

	public int getPrice() {
		return price;
	}
}
